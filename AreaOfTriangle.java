package com.edu;

public class AreaOfTriangle {
     public static void main(String[] args) {
	        int base, height;
	        float area;
	        base = 5;
	        height = 10;
	        
	        area = 0.5f * base * height;
	        System.out.println("The area of triangle of base = " + base + " and height = " + height + " is " + area);
	    }
	}

	
	